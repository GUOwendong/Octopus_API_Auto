def generate_phone() -> str:
    """生成 11 位手机号（以 1 开头）"""
    return "1" + "".join(str(random.randint(0, 9)) for _ in range(10))


def generate_name() -> str:
    """生成随机中文姓名"""
    return random.choice(SURNAMES) + random.choice(GIVEN_NAMES)


def generate_address() -> str:
    """生成完整地址：省市区 + 详细地址"""
    location = random.choice(LOCATIONS)
    street = random.choice(STREETS)
    number = random.randint(1, 999)

    # 约 70% 的概率生成详细到室的信息
    if random.random() < 0.7:
        compound = random.choice(COMPOUNDS)
        building = random.randint(1, 28)
        unit = random.randint(1, 6)
        room = random.randint(101, 2801)
        detail = f"{street}{number}号 {compound}{building}栋{unit}单元{room}室"
    else:
        detail = f"{street}{number}号"

    return location + detail


def generate_product() -> str:
    """随机选取商品"""
    return random.choice(PRODUCTS)


def generate_quantity() -> int:
    """生成 1～10 的随机数量"""
    return random.randint(1, 10)


# ========== 对外提供的核心函数 ==========
def generate_order_excel(row_count=ROW_COUNT, output_dir=None):
    """
    生成订单 Excel，返回 (文件完整路径, 订单数据列表)
    订单数据列表每个元素：{"收件人": xx, "收件人电话": xx, "商品名称": xx}
    """
    if output_dir is None:
        output_dir = DATA_DIR

    # 生成数据
    data = []
    for _ in range(row_count):
        data.append(
            {
                "收件人": generate_name(),
                "收件人电话": generate_phone(),
                "省市区+详细地址": generate_address(),
                "商品名称": generate_product(),
                "数量": generate_quantity(),
            }
        )

    df = pd.DataFrame(data)

    # 文件名带时间戳
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    filename = f"随机订单数据_{timestamp}.xlsx"

    os.makedirs(output_dir, exist_ok=True)
    file_path = os.path.join(output_dir, filename)

    df.to_excel(file_path, index=False, engine="openpyxl")

    # 提取测试需要的字段
    order_list = df[["收件人", "收件人电话", "商品名称"]].to_dict(orient="records")
    return file_path, order_list


# ========== 直接运行脚本时执行 ==========
def main():
    file_path, orders = generate_order_excel(ROW_COUNT)
    print(f"✅ 已生成 Excel 文件：{file_path}")
    print(f"📊 共 {len(orders)} 条数据")
    print("📋 前3条数据预览：")
    for i, order in enumerate(orders[:3], 1):
        print(f"  {i}. {order['收件人']} | {order['收件人电话']} | {order['商品名称']}")


if __name__ == "__main__":
    main()
